export function findEmployeeById(id: string): Employee | undefined {
  return employees.find((e) => e.id === id);
}

export function searchEmployees(filters: {
  name?: string;
  department?: string;
  clearance?: number;
}): Employee[] {
  return employees.filter((e) => {
    if (filters.name && !e.name.toLowerCase().includes(filters.name.toLowerCase())) return false;
    if (filters.department && e.department.toLowerCase() !== filters.department.toLowerCase()) return false;
    if (filters.clearance && e.securityClearance !== filters.clearance) return false;
    return true;
  });
}
