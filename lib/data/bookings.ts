export function searchBookings(filters: {
  roomId?: string;
  bookedBy?: string;
  startDate?: string;
  endDate?: string;
}): Booking[] {
  return bookings.filter((b) => {
    if (filters.roomId && b.roomId !== filters.roomId) return false;
    if (filters.bookedBy && b.bookedBy !== filters.bookedBy) return false;
    if (filters.startDate && new Date(b.endTime) < new Date(filters.startDate)) return false;
    if (filters.endDate && new Date(b.startTime) > new Date(filters.endDate)) return false;
    return true;
  });
}
