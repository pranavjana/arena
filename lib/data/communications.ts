export function searchCommunications(filters: {
  senderId?: string;
  recipientId?: string;
  search?: string;
  channel?: string;
  startDate?: string;
  endDate?: string;
}): Communication[] {
  return communications.filter((msg) => {
    if (filters.senderId && msg.senderId !== filters.senderId && msg.recipientId !== filters.senderId) return false;
    if (filters.recipientId && msg.recipientId !== filters.recipientId && msg.senderId !== filters.recipientId) return false;
    if (filters.channel && msg.channel !== filters.channel) return false;
    if (filters.search) {
      const q = filters.search.toLowerCase();
      if (!msg.subject.toLowerCase().includes(q) && !msg.body.toLowerCase().includes(q)) return false;
    }
    if (filters.startDate && new Date(msg.timestamp) < new Date(filters.startDate)) return false;
    if (filters.endDate && new Date(msg.timestamp) > new Date(filters.endDate)) return false;
    return true;
  });
}
